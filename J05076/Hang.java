package J05076;

import java.util.*;

public class Hang {
    private String ten;
    private char loai;

    public Hang(String ten, char loai) {
        this.ten = ten;
        this.loai = loai;
    }

    public String getTen() {
        return ten;
    }

    public char getLoai() {
        return loai;
    }
}
