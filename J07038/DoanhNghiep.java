package J07038;

public class DoanhNghiep {
    private String ma, ten;
    private int soSV;

    public DoanhNghiep(String ma, String ten, int soSV) {
        this.ma = ma;
        this.ten = ten;
        this.soSV = soSV;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }

    public int getSoSV() {
        return soSV;
    }

}
