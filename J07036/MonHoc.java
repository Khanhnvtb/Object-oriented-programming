package J07036;

public class MonHoc {
    private String ma, ten;
    private int tinChi;

    public MonHoc(String ma, String ten, int tinChi) {
        this.ma = ma;
        this.ten = ten;
        this.tinChi = tinChi;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }
}
